public class Ejercicio7 {
	// 7. Muestra los números del 1 al 100(ambos incluídos). Usa un bucle while.

	int numero;

	public void bucleWhile() {
		System.out.println("Imprimimos los números del 0 - 100 en una sola linea");
		while (numero <= 100) {
			System.out.print(numero + " ");
			numero++;
		}
	}
}
